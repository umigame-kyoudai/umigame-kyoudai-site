export function formatPriceWithTilde(item: { price: number; childPrice?: number }): string {
  if (item.childPrice && item.childPrice !== item.price) {
    return `¥${item.childPrice.toLocaleString()}〜`
  }
  return `¥${item.price.toLocaleString()}〜`
}

